export type Locale = "en" | "hi" | "kn" | "te" | "ta" | "ml";

export const LOCALES: { code: Locale; label: string; name: string }[] = [
  { code: "en", label: "EN", name: "English" },
  { code: "hi", label: "हिं", name: "Hindi" },
  { code: "kn", label: "ಕನ್", name: "Kannada" },
  { code: "te", label: "తెలు", name: "Telugu" },
  { code: "ta", label: "தமிழ்", name: "Tamil" },
  { code: "ml", label: "മലയാളം", name: "Malayalam" },
];

export function t(key: string, locale: Locale = "en"): string {
  return translations[locale]?.[key] || translations["en"]?.[key] || key;
}

export function getLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const stored = localStorage.getItem("nervoscan-locale");
  const valid: Locale[] = ["en", "hi", "kn", "te", "ta", "ml"];
  if (stored && valid.includes(stored as Locale)) return stored as Locale;
  return "en";
}

export function setLocale(locale: Locale) {
  if (typeof window !== "undefined") {
    localStorage.setItem("nervoscan-locale", locale);
    window.dispatchEvent(new CustomEvent("nervoscan-locale-change", { detail: locale }));
  }
}
